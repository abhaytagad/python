mystr = " ABHAY SHIVAJI TAGAD"

print(mystr.endswith("TAGAD")) # this function state whether the given strings ends the code or not

print(mystr.count("A")) # this function allows us to count the occurence of A string

print(mystr.capitalize()) # this function capitalize the string it does not take argument
print(mystr.upper()) # it gives the copy of string converted to upper case
print(mystr.lower())   # It returns the copy of the string converted to lower case.

print(mystr.find("D")) # This function finds any given character or word in the entire string. It returns the index of the first character from that word

print(mystr.replace("ABHAY","ASMITA")) # This function replaces the old word or character with a new word or character from the entire string.

